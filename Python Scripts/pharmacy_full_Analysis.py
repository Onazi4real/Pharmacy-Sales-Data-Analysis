####DASHBOARD CHARTS ANALYSIS 

#Chart 1: Revenue by Category 

revenue_category = (
df.groupby("Category")["Total_Sales"]. sum()
.sort_values(ascending=False))


plt.figure (figsize =(8, 5))
revenue_category.plot(kind="bar")

# Add revenue values on top of each bar
for i, value in enumerate(revenue_category.values):
    plt.text(
        i,
        value + 1000,        # Slightly above the bar
        f"{int(value):,}",   # Display with commas (e.g. 200,100)
        ha="center",
        fontsize=12
    )

plt.title ("Revenue by Category")
plt.xlabel ("Category")
plt.ylabel ("Total Revenue")

plt.tight_layout ()
plt.savefig ("pharmacy Revenue by Product.png")

plt.close()

