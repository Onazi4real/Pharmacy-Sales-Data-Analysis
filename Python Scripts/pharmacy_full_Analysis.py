#Already Expired Products
#Convert Expire_Date columns to datetime format 
df["Expire_Date"] = pd.to_datetime(df["Expire_Date"])

print("\nAlready Expired Products")
expired =(df[df["Expire_Date"] < pd.Timestamp.today()])

print(expired[["Product_Name", "Expire_Date", "Stock_In_Hand"]])

#Products Expiring within the next 30days
print("\nProducts Expiring within 30 Days")

today = pd.Timestamp.today()

next_30_days = today + pd.Timedelta(days=30)
expiring_soon = (
df[
(df["Expire_Date"] >= today) & 
(df["Expire_Date"] <= next_30_days)
]
)

print(expiring_soon[["Product_Name",  "Expire_Date",  "Stock_In_Hand"]])

#Days Remaining Before Expiry
df["Days_Remaining"] = (df["Expire_Date"] - pd.Timestamp.today()).dt.days

print(df[["Product_Name",  "Stock_In_Hand", "Expire_Date", "Days_Remaining"]])

